"""
Python = Linguagem de programação
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)
# Aspas simples
print('Theo Rondon')
print(1, 'Theo "Rondon"')
# Aspas duplas
print("Theo Rondon")
print(2, "Theo 'Rondon'")
# Escape
print("Theo \"Rondon\"")
# r
print(r"Theo \"Rondon\"")
