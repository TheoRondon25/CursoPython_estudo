# conversao de tipos, coerçao, type convertion, typecasting, coercion
# é o ato de converter um tipo em outro.
# tipos imutaveis e primitivos: 
# str, int, float, bool
print(int('1'), type(int('1')))
print(int('1') + 1)
print(type(float('1') + 1))
print(bool('')) # vazio - false | preenchido - true
print(str(11) + 'b')
